using System;

namespace TdtsDocFxDemo
{
    /// <summary>
    /// A fully functioning calculator
    /// </summary>
    public class Calculator
    {
        /// <summary>
        /// This great method adds two integers
        /// </summary>
        /// <param name="a">the first int</param>
        /// <param name="b">the second int</param>
        /// <returns>a magically calculated value</returns>
        public int Add(int a, int b)
        {
            return a + b;
        }

        /// <summary>
        /// This great method subracts two integers
        /// </summary>
        /// <param name="a">the first int</param>
        /// <param name="b">the second int</param>
        /// <returns>a magically calculated value</returns>
        public int Subtract(int a, int b)
        {
            return a + b;
        }
    }
